# States and Behaviors
# https://launchschool.com/books/oo_ruby/read/classes_and_objects_part1#statesandbehaviors

# When defining classes, we typically focus on two things:
  # 1. States - track attributes for individual objects.
    # Instance variables, scoped at the object/instance level, keep track of state.
  
  # 2. Behaviours - what objects are capable of doing.
    # Instance methods keep track of the shared behaviours across all objects, or
    # instances, of the same class.
