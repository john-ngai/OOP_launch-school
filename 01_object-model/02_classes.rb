# Classes Define Objects
# https://launchschool.com/books/oo_ruby/read/the_object_model

# Ruby defines the attributes and behaviours of it's objects in classes.
  # A class is defined using CamelCase to create the name.
  # File names should be in snake_case, and reflect the name of the class.


# Example of class declared using the name 'GoodDog',
# with a file name of 'good_dog.rb'.
class GoodDog
end

# Creates an instance of GoodDog and stores it in the variable 'sparky'.
# sparky is now an object or instance of the class GoodDog.
sparky = GoodDog.new

# The entire workflow of creating a new object or instance from a class
# is called 'instantiation', so it can be said that we've instantiated
# an object called 'sparky' from the class 'GoodDog'.