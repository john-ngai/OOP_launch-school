# More About Self
# https://launchschool.com/books/oo_ruby/read/classes_and_objects_part2#moreaboutself

# 1. Use 'self' when calling setter methods from within the class.
    # 'self', inside of an instance method, references the instance (object) that
    # called the method (i.e. the calling object).

# 2. Use 'self' for class method definitions.
    # 'self', outside of an instance method, references the class and can be used
    # to define class methods.

# NOTE: 'self' changes depending on the scope it is used in, so pay attention to see
# if you're inside an instance method or not!
