# Constants
# https://launchschool.com/books/oo_ruby/read/classes_and_objects_part2#constants

# Immutable variables (cannot change) are declared using constants.
  # Constants are defined with a capital first letter.
    # It is also common to make the entire variable uppercase, but not required.
# It is possible to reassign a new value to constants, but Ruby will throw an error.

# Example:
DOG_YEARS = 7
